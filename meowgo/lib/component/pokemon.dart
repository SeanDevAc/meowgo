class Pokemon {
  final String name;
  final String url;
  final String imageUrl;
  final String type;
  final bool unlocked;
  final int pokemonNumber;

  Pokemon({
    required this.name,
    required this.url,
    required this.imageUrl,
    required this.type,
    required this.unlocked,
    required this.pokemonNumber,
  });
}
